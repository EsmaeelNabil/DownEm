public class DownloadData {

    public String id;
    public String lable;

    public DownloadData(String id, String lable) {
        this.id = id;
        this.lable = lable;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLable() {
        return lable;
    }

    public void setLable(String lable) {
        this.lable = lable;
    }




}
